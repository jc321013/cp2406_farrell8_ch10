class Horse
{
    private String name;
    private String color;
    private int birthYear;
    String getName()
    {
        return name;
    }
    String getColor()
    {
        return color;
    }
    int getBirthYear()
    {
        return birthYear;
    }
    void setName(String n)
    {
        name = n;
    }
    void setColor(String c)
    {
        color = c;
    }
    void setBirthYear(int y)
    {
        birthYear = y;
    }
}

